package B_Arrays;

public class Array2 {
    public static void main(String[] args) {

//        Printing the sum of array values:
        int[] arr = {1, 2, 3, 4, 5};
        int sum = 0;
        for (int value: arr){
            sum += value;
        }
        System.out.println("The sum of array values is: "+sum);


    }
}
