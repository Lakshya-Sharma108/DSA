package B_Arrays;

public class Array3 {
    public static void main(String[] args) {


//        Printing the multiplication of array values:
        int[] arr = {2, 3, 4};
        int multi = 1;
        for (int value: arr){
            multi *= value;
        }
        System.out.println("The multiplication of array values is: "+multi);



    }
}
